"""Post-hoc RMSE undercard for the TabFM wide study.

The pre-registered study scored densities (logpdf, CRPS) with an MAE
undercard. This addendum, clearly post-hoc and run after the frozen
results were committed, rescored the same regres arm points against the
functional RMSE actually elicits: laplace's predictive MEAN (the MAE
undercard correctly used its median). Same universe, same test windows,
same TabFM points regenerated by the frozen reg_arm code path. MAE is
recomputed alongside as a reproduction check against the committed
undercard.

Usage:
    TB_DEVICE=mps PYTHONPATH=src python benchmarks/tabfm_wide_rmse.py
"""
import csv
import math
import os
import sys
import time

import numpy as np

_HERE = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, os.path.join(_HERE, ".."))
sys.path.insert(0, os.path.join(_HERE, "..", "src"))
sys.path.insert(0, _HERE)

from tabfm_wide_study import (  # noqa: E402
    load_universe, _load_ch, reg_arm, laplace_dists, _mps_shim,
    TEST, WEIGHTS, DEVICE,
)

OUT = os.path.join(_HERE, "results_tabfm_wide_rmse.csv")


def main():
    from tabfm import TabFMRegressor as Reg, tabfm_v1_0_0_pytorch as V
    model = V.load(model_type="regression", checkpoint_path=WEIGHTS,
                   device=None if DEVICE == "cpu" else DEVICE)
    _mps_shim()

    universe = load_universe()
    done = set()
    if os.path.exists(OUT):
        done = {r["series"] for r in csv.DictReader(open(OUT))}
    new = not os.path.exists(OUT)
    fh = open(OUT, "a", newline="")
    w = csv.writer(fh)
    if new:
        w.writerow(["series", "tabfm_rmse", "laplace_rmse", "tabfm_mae",
                    "laplace_mae_mean", "n", "stratum"])
        fh.flush()

    for j, (sid, stratum) in enumerate(universe):
        if sid in done:
            continue
        t0 = time.time()
        ch = _load_ch(sid)
        y = np.array(ch[len(ch) - TEST:])
        _, points = reg_arm(ch, 8, model, Reg)
        lap = laplace_dists(ch)
        lap_mean = np.array([d.mean for d in lap])
        tab = np.array(points)
        res = [sid,
               float(np.sqrt(np.mean((tab - y) ** 2))),
               float(np.sqrt(np.mean((lap_mean - y) ** 2))),
               float(np.mean(np.abs(tab - y))),
               float(np.mean(np.abs(lap_mean - y))),
               len(y), stratum]
        w.writerow(res)
        fh.flush()
        os.fsync(fh.fileno())
        print(f"  rmse {j + 1}/{len(universe)} {sid} ({time.time() - t0:.0f}s)",
              flush=True)
    fh.close()

    rows = list(csv.DictReader(open(OUT)))
    n = len(rows)
    lw = sum(1 for r in rows
             if float(r["laplace_rmse"]) < float(r["tabfm_rmse"]))
    print(f"\nRMSE undercard (regres, laplace mean): laplace wins "
          f"{100.0 * lw / n:.0f}% of {n}")
    by = {}
    for r in rows:
        rep = r["stratum"].split("|")[1]
        b = by.setdefault(rep, [0, 0])
        b[0] += 1
        b[1] += float(r["laplace_rmse"]) < float(r["tabfm_rmse"])
    for rep in ("cts", "mixed", "repeat"):
        if rep in by:
            t, wn = by[rep]
            print(f"  {rep:8s} n={t:3d}  laplace wins {100.0 * wn / t:.0f}%")


if __name__ == "__main__":
    main()
