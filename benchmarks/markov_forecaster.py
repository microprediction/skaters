"""markov: a machine-generated one-step distributional forecaster.

Named in the package convention (after laplace) for its signature mechanism,
regime switching. Generated by a language model inside the inspiration-
simplex study (humpday repo, papers/dfo_recommender, artifact
runs/e22_adaptive_code/pure_RegimeWatcher.py, copied verbatim as
markov_artifact.py). On fifty fresh synthetic instances it beat laplace
pairwise 82% (p = 6e-6); this module registers it in the one honest harness
so the claim meets real FRED data.

Design: RLS-AR(3) and exponential-smoothing means mixed by decayed
log-score softmax; GARCH(1,1) and EWMA variances mixed the same way;
Student-t density with kurtosis-tracked degrees of freedom; an EWMA break
monitor that inflates variance and resets state.

The skater adapter discretizes the Student-t predictive into a Gaussian
scale mixture via Wilson-Hilferty chi-square quantiles, so the Dist algebra
(logpdf, crps) sees faithful heavy tails.
"""
from __future__ import annotations

import math

from skaters.dist import Dist

_MARKOV_SOURCE = open(__file__.replace("markov_forecaster.py",
                                     "markov_artifact.py")).read()
_ns: dict = {}
exec(compile(_MARKOV_SOURCE, "<star>", "exec"), _ns)  # noqa: S102
MarkovForecaster = _ns["Forecaster"]


def _chi2_quantile(nu: float, p: float) -> float:
    """Wilson-Hilferty approximation."""
    from statistics import NormalDist

    z = NormalDist().inv_cdf(p)
    a = 2.0 / (9.0 * nu)
    return nu * (1.0 - a + z * math.sqrt(a)) ** 3


def t_as_dist(mu: float, var: float, nu: float, m: int = 7) -> Dist:
    """Student-t(mu, var, nu) as an m-component Gaussian scale mixture.

    t_nu is N(0, s^2 * g) with g ~ nu / chi2_nu; discretize g at mid-quantiles.
    """
    nu = max(2.6, min(200.0, nu))
    s2 = max(var, 1e-12) * (nu - 2.0) / nu
    comps = []
    for j in range(m):
        p = (j + 0.5) / m
        g = nu / max(_chi2_quantile(nu, 1.0 - p), 1e-9)
        comps.append((1.0 / m, mu, math.sqrt(s2 * g)))
    return Dist(comps)


def markov_skater():
    """factory() -> skater f(y, state) -> ([Dist], state), one-step."""

    def f(y, state):
        fc = state["fc"] if state else MarkovForecaster()
        fc.update(y)
        pr = fc._predict()
        return [t_as_dist(pr["mu"], pr["var"], pr["nu"])], {"fc": fc}

    return f


def markov_mix_skater():
    """Decayed log-score softmax mixture of markov and laplace, as one Dist.

    laplace runs without the GPD splice here (SplicedDist is not a component
    mixture, so it cannot enter Dist.combine); the mixture's heavy tails come
    from markov's t-mixture side."""
    from skaters.api import laplace

    DECAY = 0.98

    def f(y, state):
        if state is None:
            state = {"fc": MarkovForecaster(), "lp_state": None,
                     "lap": laplace(1, tails="gaussian"), "cs": 0.0, "cl": 0.0,
                     "pend_s": None, "pend_l": None}
        # score the pending predictions against the arriving y
        if state["pend_s"] is not None:
            ls = state["pend_s"].logpdf(y)
            ll = state["pend_l"].logpdf(y)
            ls = ls if math.isfinite(ls) else -20.0
            ll = ll if math.isfinite(ll) else -20.0
            state["cs"] = DECAY * state["cs"] + ls
            state["cl"] = DECAY * state["cl"] + ll
            m = max(state["cs"], state["cl"])
            state["cs"] -= m
            state["cl"] -= m
        # update both models
        state["fc"].update(y)
        pr = state["fc"]._predict()
        d_star = t_as_dist(pr["mu"], pr["var"], pr["nu"])
        dists_l, state["lp_state"] = state["lap"](y, state["lp_state"])
        d_lap = dists_l[0]
        state["pend_s"], state["pend_l"] = d_star, d_lap
        es = math.exp(min(50.0, state["cs"]))
        el = math.exp(min(50.0, state["cl"]))
        w = es / (es + el)
        return [Dist.combine([d_star, d_lap], [w, 1.0 - w])], state

    return f
