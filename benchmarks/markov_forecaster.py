"""markov: a machine-generated one-step distributional forecaster.

Named in the package convention (after laplace) for its signature mechanism,
regime switching. Generated by a language model inside the inspiration-
simplex study (humpday repo, papers/dfo_recommender, artifact
runs/e22_adaptive_code/pure_RegimeWatcher.py, copied verbatim as
markov_artifact.py). On fifty fresh synthetic instances it beat laplace
pairwise 82% (p = 6e-6); this module registers it in the one honest harness
so the claim meets real FRED data.

Design: RLS-AR(3) and exponential-smoothing means mixed by decayed
log-score softmax; GARCH(1,1) and EWMA variances mixed the same way;
Student-t density with kurtosis-tracked degrees of freedom; an EWMA break
monitor that inflates variance and resets state.

The skater adapter discretizes the Student-t predictive into a Gaussian
scale mixture via Wilson-Hilferty chi-square quantiles, so the Dist algebra
(logpdf, crps) sees faithful heavy tails.
"""
from __future__ import annotations

import math

from skaters.dist import Dist

from skaters.markov import Forecaster as MarkovForecaster  # noqa: E402
from skaters.markov import markov_drift as _src_markov_drift  # noqa: E402


def _chi2_quantile(nu: float, p: float) -> float:
    """Wilson-Hilferty approximation."""
    from statistics import NormalDist

    z = NormalDist().inv_cdf(p)
    a = 2.0 / (9.0 * nu)
    return nu * (1.0 - a + z * math.sqrt(a)) ** 3


def t_as_dist(mu: float, var: float, nu: float, m: int = 7) -> Dist:
    """Student-t(mu, var, nu) as an m-component Gaussian scale mixture.

    t_nu is N(0, s^2 * g) with g ~ nu / chi2_nu; discretize g at mid-quantiles.
    """
    nu = max(2.6, min(200.0, nu))
    s2 = max(var, 1e-12) * (nu - 2.0) / nu
    comps = []
    for j in range(m):
        p = (j + 0.5) / m
        g = nu / max(_chi2_quantile(nu, 1.0 - p), 1e-9)
        comps.append((1.0 / m, mu, math.sqrt(s2 * g)))
    return Dist(comps)


def markov_skater():
    """factory() -> skater f(y, state) -> ([Dist], state), one-step."""

    def f(y, state):
        fc = state["fc"] if state else MarkovForecaster()
        fc.update(y)
        pr = fc._predict()
        return [t_as_dist(pr["mu"], pr["var"], pr["nu"])], {"fc": fc}

    return f


def markov_mix_skater():
    """Decayed log-score softmax mixture of markov and laplace, as one Dist.

    laplace runs without the GPD splice here (SplicedDist is not a component
    mixture, so it cannot enter Dist.combine); the mixture's heavy tails come
    from markov's t-mixture side."""
    from skaters.api import laplace

    DECAY = 0.98

    def f(y, state):
        if state is None:
            state = {"fc": MarkovForecaster(), "lp_state": None,
                     "lap": laplace(1, tails="gaussian"), "cs": 0.0, "cl": 0.0,
                     "pend_s": None, "pend_l": None}
        # score the pending predictions against the arriving y
        if state["pend_s"] is not None:
            ls = state["pend_s"].logpdf(y)
            ll = state["pend_l"].logpdf(y)
            ls = ls if math.isfinite(ls) else -20.0
            ll = ll if math.isfinite(ll) else -20.0
            state["cs"] = DECAY * state["cs"] + ls
            state["cl"] = DECAY * state["cl"] + ll
            m = max(state["cs"], state["cl"])
            state["cs"] -= m
            state["cl"] -= m
        # update both models
        state["fc"].update(y)
        pr = state["fc"]._predict()
        d_star = t_as_dist(pr["mu"], pr["var"], pr["nu"])
        dists_l, state["lp_state"] = state["lap"](y, state["lp_state"])
        d_lap = dists_l[0]
        state["pend_s"], state["pend_l"] = d_star, d_lap
        es = math.exp(min(50.0, state["cs"]))
        el = math.exp(min(50.0, state["cl"]))
        w = es / (es + el)
        return [Dist.combine([d_star, d_lap], [w, 1.0 - w])], state

    return f


class _Shifted:
    """Exact mean-shift proxy for any Dist-like predictive (incl. SplicedDist)."""

    def __init__(self, base, delta):
        self.base = base
        self.delta = delta

    def logpdf(self, y):
        return self.base.logpdf(y - self.delta)

    def crps(self, y):
        return self.base.crps(y - self.delta)

    def cdf(self, y):
        return self.base.cdf(y - self.delta)

    @property
    def mean(self):
        return self.base.mean + self.delta

    @property
    def std(self):
        return self.base.std


def markov_nudge_skater(eta=0.02, decay=0.999, cap=0.5):
    """laplace, with its predictive mean cautiously nudged by markov features.

    markov runs as a shadow emitting features per tick: the gap between its
    conditional mean and laplace's, and its regime state. A normalized-LMS
    regression with tiny learning rate, geometric shrinkage toward zero, and
    a nudge cap of `cap` laplace standard deviations learns how much of the
    gap to trust. Coefficients start at zero: on series where markov carries
    no signal the coupling stays asleep and laplace is untouched.
    """
    from skaters.api import laplace

    def f(y, state):
        if state is None:
            state = {"fc": MarkovForecaster(), "lap": laplace(1),
                     "lp_state": None, "theta": [0.0, 0.0], "pend": None,
                     "recent": [], "prev": None}
        # lattice guard: on repeating/discrete series the lattice projection is
        # exact and any mean shift is catastrophic; the nudge sleeps there.
        state["recent"].append(1.0 if state["prev"] is not None and y == state["prev"] else 0.0)
        state["prev"] = y
        if len(state["recent"]) > 200:
            state["recent"].pop(0)
        lattice_like = (sum(state["recent"]) / max(1, len(state["recent"]))) > 0.10
        # learn from the arriving y against the previously issued prediction.
        # Features are standardized by laplace's own scale, so theta is
        # dimensionless; the +1.0 in the normalizer makes the update genuinely
        # cautious when the models agree (features near zero).
        if state["pend"] is not None:
            m_l, sd_l, z = state["pend"]
            err_z = (y - m_l) / sd_l - min(cap, max(-cap,
                     state["theta"][0] * z[0] + state["theta"][1] * z[1]))
            nz = z[0] * z[0] + z[1] * z[1] + 1.0
            for i in (0, 1):
                t = decay * state["theta"][i] + eta * err_z * z[i] / nz
                state["theta"][i] = max(-1.0, min(1.0, t))
        # advance both models
        state["fc"].update(y)
        pr = state["fc"]._predict()
        dists, state["lp_state"] = state["lap"](y, state["lp_state"])
        d = dists[0]
        m_l, sd_l = d.mean, max(d.std, 1e-9)
        gap = max(-3.0, min(3.0, (pr["mu"] - m_l) / sd_l))
        z = (gap, max(-3.0, min(3.0, (state["fc"].inflation - 1.0) * gap)))
        nudge_z = min(cap, max(-cap, state["theta"][0] * z[0] + state["theta"][1] * z[1]))
        if lattice_like:
            nudge_z = 0.0
        state["pend"] = (m_l, sd_l, z)
        return [_Shifted(d, nudge_z * sd_l)], state

    return f


def markov_nudge_pre_skater(sticky=True, tails=True, eta=0.02, decay=0.999, cap=0.5):
    """The nudge applied where it belongs: on the smooth pre-lattice trunk.

    Pipeline: candidate trunk -> cautious markov nudge (mean shift on plain
    Dists, exact) -> lattice projection (re-snaps around the nudged mean) ->
    GPD tails. `sticky=False`/`tails=False` give the ablation arms so the
    nudge effect can be isolated from the projection.
    """
    from skaters.api import _build_candidates, _objective_leaf
    from skaters.sticky import sticky as _project
    from skaters.tails import gpdtails
    from skaters.terminal import terminal_leaf_ensemble

    def build_trunk():
        candidates, depths, _ = _build_candidates(1)
        return terminal_leaf_ensemble(
            candidates, k=1, leaf_fn=_objective_leaf("crps", 0.03),
            learning_rate=0.8, complexity_penalty=0.005, depths=depths,
            max_components=20, forget=0.99)

    def nudged(trunk):
        def f(y, state):
            if state is None:
                state = {"ts": None, "fc": MarkovForecaster(),
                         "theta": [0.0, 0.0], "pend": None,
                         "recent": [], "prev": None}
            # lattice guard: where exact repeats dominate, the projection's
            # atoms carry the score and any trunk perturbation is pure harm;
            # the nudge sleeps and the pipeline reduces to plain laplace.
            state["recent"].append(
                1.0 if state["prev"] is not None and y == state["prev"] else 0.0)
            state["prev"] = y
            if len(state["recent"]) > 200:
                state["recent"].pop(0)
            asleep = (sum(state["recent"]) / max(1, len(state["recent"]))) > 0.10
            if state["pend"] is not None:
                m_l, sd_l, z = state["pend"]
                err_z = (y - m_l) / sd_l - min(cap, max(-cap,
                         state["theta"][0] * z[0] + state["theta"][1] * z[1]))
                nz = z[0] * z[0] + z[1] * z[1] + 1.0
                for i in (0, 1):
                    t = decay * state["theta"][i] + eta * err_z * z[i] / nz
                    state["theta"][i] = max(-1.0, min(1.0, t))
            state["fc"].update(y)
            pr = state["fc"]._predict()
            dists, state["ts"] = trunk(y, state["ts"])
            d = dists[0]
            m_l, sd_l = d.mean, max(d.std, 1e-9)
            gap = max(-3.0, min(3.0, (pr["mu"] - m_l) / sd_l))
            z = (gap, max(-3.0, min(3.0, (state["fc"].inflation - 1.0) * gap)))
            nudge_z = min(cap, max(-cap,
                          state["theta"][0] * z[0] + state["theta"][1] * z[1]))
            if asleep:
                nudge_z = 0.0
            state["pend"] = (m_l, sd_l, z)
            return [d.shift(nudge_z * sd_l)], state
        return f

    f = nudged(build_trunk())
    if sticky:
        f = _project(f, k=1)
    if tails:
        f = gpdtails(f, k=1)
    return f


markov_drift = _src_markov_drift


def laplace_star_skater():
    """laplace's single-scale stack with one extra candidate: the markov_drift
    transform conjugated over the standard leaf. No API changes; the frozen
    laplace signature is untouched — this is population growth by the
    sanctioned mechanism (a new transform), composed in benchmark space."""
    from skaters.api import _build_candidates, _objective_leaf
    from skaters.conjugate import conjugate
    from skaters.leaf import leaf
    from skaters.sticky import sticky as _project
    from skaters.tails import gpdtails
    from skaters.terminal import terminal_leaf_ensemble

    candidates, depths, _ = _build_candidates(1)
    candidates.append(conjugate(leaf(k=1), markov_drift(), k=1))
    depths.append(2)
    f = terminal_leaf_ensemble(candidates, k=1,
                               leaf_fn=_objective_leaf("crps", 0.03),
                               learning_rate=0.8, complexity_penalty=0.005,
                               depths=depths, max_components=20, forget=0.99)
    return gpdtails(_project(f, k=1), k=1)
